package operators;

public class Arithmetic {

	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 50;
		
		int sum = num1 + num2;
		System.out.println("sum of given numbers : "+sum);
//		System.out.println("sum of given numbers : "+(num1+num2));
		
		int sub = num1 - num2;
		System.out.println("sub of given numbers : "+sub);
		
		int mul = num1 * num2;
		System.out.println("mul of given numbers : "+mul);
		
		int div = num1 / num2;
		System.out.println("div of given numbers : "+div);
		
		int num3 = 10;
		int num4 = 3;
		int modulus = num3 % num4;
		System.out.println("remainder of given numbers : "+modulus);
		
		String firstName = "Shantosh "; 
		String lastName = "Kumar";
		String name = firstName + lastName;
		System.out.println("joint string : "+name);

	}

}














